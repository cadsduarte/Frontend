public class OperadoresRelacionais {
    
    public static void main(String[] args) {
        
        //variaveis
        int numero1 = 10;
        int numero2 = 5;

        //Operadores relacionais
        System.out.println(numero1 > numero2);
        System.out.println(numero1 >= numero2);
         System.out.println(numero1 < numero2);
        System.out.println(numero1 <= numero2);
        System.out.println(numero1 == numero2);
        System.out.println(numero1 != numero2);
    }
}
